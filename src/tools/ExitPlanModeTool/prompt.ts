const ASK_USER_QUESTION_TOOL_NAME = 'AskUserQuestion'

export const EXIT_PLAN_MODE_V2_TOOL_PROMPT = `当您在计划模式下并已完成将计划写入计划文件、准备获得用户批准时，使用此工具。

## 此工具的工作原理
- 您应该已经将计划写入计划模式系统消息中指定的计划文件
- 此工具不接受计划内容作为参数 - 它将从您写入的文件中读取计划
- 此工具只是表示您已完成计划，准备好让用户查看和批准
- 用户在查看计划时将看到您计划文件的内容

## 何时使用此工具
重要提示：仅当任务需要规划需要编写代码的任务的实施步骤时，才使用此工具。对于您正在收集信息、搜索文件、读取文件或通常试图理解代码库的研究任务 - 不要使用此工具。

## 使用此工具之前
确保您的计划完整且明确：
- 如果您有关于需求或方法的未解决问题，请先使用 ${ASK_USER_QUESTION_TOOL_NAME}（在早期阶段）
- 一旦您的计划最终确定，使用此工具请求批准

**重要：** 不要使用 ${ASK_USER_QUESTION_TOOL_NAME} 询问"这个计划可以吗？"或"我应该继续吗？" — 这正是此工具的作用。ExitPlanMode 固有地请求用户批准您的计划。

## 示例

1. 初始任务："搜索并理解代码库中 vim 模式的实现" - 不要使用退出计划模式工具，因为您不是在规划任务的实施步骤。
2. 初始任务："帮我为 vim 实现 yank 模式" - 在完成任务的实施步骤规划后，使用退出计划模式工具。
3. 初始任务："添加处理用户身份验证的新功能" - 如果不确定身份验证方法（OAuth、JWT 等），请先使用 ${ASK_USER_QUESTION_TOOL_NAME}，然后在澄清方法后使用退出计划模式工具。
`
